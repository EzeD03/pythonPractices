print("Convertidor de numeros a letras.")

num_uno = int(input("¿Cuál es el número que deseas convertir?: "))

if num_uno == 1:
    print("Uno")
elif num_uno == 2:
    print("Dos")
elif num_uno == 3:
    print("Tres")
elif num_uno == 4:
    print("Cuatro")
elif num_uno == 5:
    print("Cinco")
else:
    print("Lo sentimos, este convertidor solo ofrece convertir hasta el número 5")

